package com.sonrisa.swarm.posintegration.extractor.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * This annotation can be used to mark fields on a swarm entity
 * and to define how its value is mapped to an External (e.g. Erply) entity
 *
 * @author joe
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.METHOD)
public @interface ExternalField {
    
    /** External (e.g. Erply) JSON or XML field key*/
    String value();
    
    /**
     * If set to true, extraction will halt if value is missing
     */
    boolean required() default false;
}
