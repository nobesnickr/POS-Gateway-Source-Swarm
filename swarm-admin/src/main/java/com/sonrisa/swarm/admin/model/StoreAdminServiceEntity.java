package com.sonrisa.swarm.admin.model;

/**
 * Entity used by {@link StoreAdminService} to update stores.
 */
public class StoreAdminServiceEntity {

    /**
     * Store's name
     */
    private String name;
    
    /**
     * Value indicating whether store is active
     */
    private Boolean active;
    
    /**
     * Notes for the store
     */
    private String notes;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Boolean getActive() {
        return active;
    }

    public void setActive(Boolean active) {
        this.active = active;
    }

    public String getNotes() {
        return notes;
    }

    public void setNotes(String notes) {
        this.notes = notes;
    }

    @Override
    public String toString() {
        return "StoreAdminServiceEntity [name=" + name + ", active=" + active + ", notes=" + notes + "]";
    }
}
