package com.sonrisa.swarm.lspro.controller.entity;

import com.fasterxml.jackson.annotation.JsonProperty;
import com.sonrisa.swarm.lspro.controller.LsProStoreController;

/**
 * Entity sent to {@link LsProStoreController} when attempting the register new store
 */
public class LsProAccountEntity {

    /**
     * Account's userName for Basic Authentication
     */
    @JsonProperty("user")
    private String userName;
    
    /**
     * Account's password for Basic Authentication
     */
    @JsonProperty("token")
    private String token;

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }
}
