package com.sonrisa.swarm.controller.entity;

import com.fasterxml.jackson.annotation.JsonProperty;
import com.sonrisa.swarm.controller.RicsStoreController;

/**
 * Response from the {@link RicsStoreController} controller.
 */
public class RicsAccountResponseEntity {

    /**
     * Store's id in the swarm DB
     */
    @JsonProperty("store_id")
    private Long storeId;
    
    /**
     * Store's name
     */
    private String name;
    
    /**
     * Store code
     */
    @JsonProperty("store_code")
    private String storeCode;

    public Long getStoreId() {
        return storeId;
    }

    public void setStoreId(Long storeId) {
        this.storeId = storeId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getStoreCode() {
        return storeCode;
    }

    public void setStoreCode(String storeCode) {
        this.storeCode = storeCode;
    }
}
